import { strictEqual } from "node:assert";
import { spawnSync } from "node:child_process";
import { createHash } from "node:crypto";
import {
  mkdirSync,
  readFileSync,
  readdirSync,
  rmSync,
  writeFileSync
} from "node:fs";
import { fileURLToPath } from "node:url";
import path from "node:path";

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const repoRoot = path.resolve(__dirname, "../..");
const workspaceRoot = path.join(repoRoot, ".tmp/dependency-plan-benchmark");
const edgeCounts = [128, 256, 512] as const;
const measuredRuns = 5;

interface Measurement {
  readonly edges: number;
  readonly runtimeEdges: number;
  readonly typeEdges: number;
  readonly medianMilliseconds: number;
  readonly samples: ReadonlyArray<number>;
  readonly outputHash: string;
}

function padded(value: number): string {
  return value.toString().padStart(3, "0");
}

/**
 * Creates one deliberately large, but otherwise ordinary, Haxe module.
 *
 * Why: dependency planning happens inside a real compiler build. Calling an
 * internal helper in isolation would skip Haxe typing, dead-code elimination,
 * and the same import facts that production code supplies.
 *
 * What: half of the generated externs are read by executable code, so they
 * become runtime imports. The other half appear only in kept method types, so
 * genes-ts must plan TypeScript-only imports as well.
 *
 * How: every extern uses a unique package specifier. Increasing `edgeCount`
 * therefore increases the number of independent dependency requests without
 * adding unrelated application logic. The generated source lives under
 * `.tmp/` and is never a shipped compiler fixture.
 */
function sourceFor(edgeCount: number): string {
  const runtimeEdges = edgeCount / 2;
  const typeEdges = edgeCount - runtimeEdges;
  const lines = [
    "package dependencyplanbenchmark;",
    "",
    "/** Test-only entrypoint generated by benchmark-dependency-plan.ts. */",
    "@:keep",
    "class Main {",
    "  static function main():Void {",
    "    var total = 0;"
  ];

  for (let index = 0; index < runtimeEdges; index++) {
    lines.push(`    total += RuntimeEdge${padded(index)}.read();`);
  }
  lines.push(
    "    if (total == -1)",
    "      trace(total);",
    "  }",
    ""
  );
  for (let index = 0; index < typeEdges; index++) {
    lines.push(
      `  public static function keepType${padded(index)}(`
      + `value:TypeEdge${padded(index)}):TypeEdge${padded(index)} {`,
      "    return value;",
      "  }",
      ""
    );
  }
  lines.push("}", "");

  for (let index = 0; index < runtimeEdges; index++) {
    lines.push(
      `@:jsRequire("dependency-plan-runtime-${padded(index)}", "RuntimeEdge${padded(index)}")`,
      `extern class RuntimeEdge${padded(index)} {`,
      "  public static function read():Int;",
      "}",
      ""
    );
  }
  for (let index = 0; index < typeEdges; index++) {
    lines.push(
      `@:jsRequire("dependency-plan-type-${padded(index)}", "TypeEdge${padded(index)}")`,
      `extern class TypeEdge${padded(index)} {}`,
      ""
    );
  }
  return `${lines.join("\n")}\n`;
}

function filesUnder(root: string): string[] {
  const files: string[] = [];
  function visit(directory: string): void {
    for (const entry of readdirSync(directory, { withFileTypes: true })) {
      const absolute = path.join(directory, entry.name);
      if (entry.isDirectory())
        visit(absolute);
      else if (entry.isFile())
        files.push(absolute);
    }
  }
  visit(root);
  return files.sort();
}

/** Hashes paths as well as bytes so a renamed output cannot look unchanged. */
function treeHash(root: string): string {
  const hash = createHash("sha256");
  for (const file of filesUnder(root)) {
    hash.update(path.relative(root, file).replaceAll("\\", "/"));
    hash.update("\0");
    hash.update(readFileSync(file));
    hash.update("\0");
  }
  return hash.digest("hex");
}

function compile(sourceRoot: string, outputRoot: string): number {
  const started = performance.now();
  const result = spawnSync("haxe", [
    "-lib", "genes-ts",
    "-cp", sourceRoot,
    "--main", "dependencyplanbenchmark.Main",
    "-js", path.join(outputRoot, "index.ts"),
    "-D", "genes.ts",
    "-D", "no-deprecation-warnings",
    "-D", "js-es=6",
    "-dce", "full"
  ], {
    cwd: repoRoot,
    encoding: "utf8"
  });
  const elapsed = performance.now() - started;
  if (result.status !== 0) {
    throw new Error(
      `Haxe failed with status ${result.status ?? "unknown"}\n`
      + `${result.stdout}\n${result.stderr}`
    );
  }
  return elapsed;
}

function haxeVersion(): string {
  const result = spawnSync("haxe", ["--version"], { encoding: "utf8" });
  if (result.status !== 0) {
    throw new Error(
      `Unable to read the Haxe version (status ${result.status ?? "unknown"})\n`
      + `${result.stdout}\n${result.stderr}`
    );
  }
  return result.stdout.trim();
}

function verifyEdgeInventory(outputRoot: string, expected: number): void {
  const output = filesUnder(outputRoot)
    .map((file) => readFileSync(file, "utf8"))
    .join("\n");
  const matches = output.match(/dependency-plan-(?:runtime|type)-\d{3}/g) ?? [];
  strictEqual(matches.length, expected,
    `expected ${expected} dependency declarations in generated output`);
  strictEqual(new Set(matches).size, expected,
    `expected ${expected} distinct dependency requests in generated output`);
  const expectedPerKind = expected / 2;
  const runtimeImports = output.match(
    /^import (?!type ).*dependency-plan-runtime-\d{3}.*$/gm
  ) ?? [];
  const typeImports = output.match(
    /^import type .*dependency-plan-type-\d{3}.*$/gm
  ) ?? [];
  strictEqual(runtimeImports.length, expectedPerKind,
    `expected ${expectedPerKind} executable imports`);
  strictEqual(typeImports.length, expectedPerKind,
    `expected ${expectedPerKind} TypeScript-only imports`);
}

function median(values: ReadonlyArray<number>): number {
  const ordered = [...values].sort((left, right) => left - right);
  return ordered[Math.floor(ordered.length / 2)] ?? 0;
}

/**
 * Measures complete warm compiler builds rather than claiming a micro-timing.
 *
 * One unmeasured build warms filesystem caches and creates the output tree.
 * Five fresh Haxe processes then compile the same source. This includes Haxe
 * parsing, typing, dependency planning, and printing, so the result answers the
 * practical question “does a larger import graph make builds grow sharply?”
 * It does not claim to isolate one function inside `DependencyPlan`.
 */
function measure(edgeCount: number): Measurement {
  const caseRoot = path.join(workspaceRoot, `${edgeCount}`);
  const sourceRoot = path.join(caseRoot, "src");
  const outputRoot = path.join(caseRoot, "out");
  const sourceDirectory = path.join(sourceRoot, "dependencyplanbenchmark");
  mkdirSync(sourceDirectory, { recursive: true });
  mkdirSync(outputRoot, { recursive: true });
  writeFileSync(path.join(sourceDirectory, "Main.hx"), sourceFor(edgeCount));

  compile(sourceRoot, outputRoot);
  verifyEdgeInventory(outputRoot, edgeCount);
  const expectedHash = treeHash(outputRoot);
  const samples: number[] = [];
  for (let run = 0; run < measuredRuns; run++) {
    samples.push(compile(sourceRoot, outputRoot));
    strictEqual(treeHash(outputRoot), expectedHash,
      `${edgeCount}-edge output changed between identical builds`);
  }

  return {
    edges: edgeCount,
    runtimeEdges: edgeCount / 2,
    typeEdges: edgeCount / 2,
    medianMilliseconds: median(samples),
    samples,
    outputHash: expectedHash
  };
}

function formatMilliseconds(value: number): string {
  return value.toFixed(1);
}

function main(): void {
  rmSync(workspaceRoot, { recursive: true, force: true });
  try {
    const measurements = edgeCounts.map(measure);
    process.stdout.write(
      "dependency-plan-benchmark:report-only\n"
      + `Environment: Node ${process.versions.node}; Haxe ${haxeVersion()}; `
      + `${process.platform}/${process.arch}\n`
      + "Measures whole warm genes-ts builds; no timing threshold is enforced.\n"
    );
    for (const measurement of measurements) {
      process.stdout.write(
        `${measurement.edges} edges `
        + `(${measurement.runtimeEdges} runtime + ${measurement.typeEdges} type-only): `
        + `median=${formatMilliseconds(measurement.medianMilliseconds)}ms; `
        + `samples=${measurement.samples.map(formatMilliseconds).join(",")}; `
        + `sha256=${measurement.outputHash}\n`
      );
    }
    const first = measurements[0];
    const last = measurements[measurements.length - 1];
    if (first != null && last != null) {
      process.stdout.write(
        `largest/smallest median ratio=`
        + `${(last.medianMilliseconds / first.medianMilliseconds).toFixed(2)}x `
        + `for ${(last.edges / first.edges).toFixed(0)}x as many edges\n`
      );
    }
  } finally {
    // A successful report contains everything needed for comparison. Remove
    // generated sources and output so running a benchmark never dirties the
    // checkout or leaves hundreds of misleading fixture files behind.
    rmSync(workspaceRoot, { recursive: true, force: true });
  }
}

main();
